    private void loadstock() {
        try {
            ResultSet data = MySQL.search("SELECT DISTINCT `stock`.`id`,`item`.`id`,`measurement`,`stock`.`individual_pieces`,`stock`.`a_individual_pieces`,`stock`.`qty`,`grn_item`.`buying_price`,`stock`.`selling_price`,`stock`.`room_id`,`stock`.`MFD`,`stock`.`EXD`,`item_category`.`name`,`item_category_type`.`name`,`item`.`name`,`grn`.`datetime` FROM `stock` INNER JOIN `item` ON `stock`.`item_id` = `item`.`id` INNER JOIN `grn_item` ON `stock`.`id` = `grn_item`.`stock_id` INNER JOIN `grn` ON `grn_item`.`grn_id` =`grn`.`id` INNER JOIN `item_category` ON `item`.`item_category_id` = `item_category`.`id` INNER JOIN `item_category_type` ON `item`.`item_category_type_id` = `item_category_type`.`id`  ORDER BY `grn`.`datetime` ASC;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (data.next()) {
                Vector v = new Vector();
                v.add(data.getString("item.id"));
                v.add(data.getString("item.name"));
                v.add(data.getString("item_category_type.name"));
                v.add(data.getString("item_category.name"));
                v.add(data.getString("stock.id"));
                v.add(data.getString("stock.measurement"));
                v.add(data.getString("stock.individual_pieces"));
                v.add(data.getString("stock.a_individual_pieces"));
                v.add(data.getString("stock.qty"));
                v.add(data.getString("grn_item.buying_price"));
                v.add(data.getString("stock.selling_price"));
                v.add(data.getString("stock.room_id"));
                v.add(data.getString("stock.MFD"));
                v.add(data.getString("stock.EXD"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

