    private void loadBranch() {
        try {

            ResultSet data = MySQL.search("SELECT * FROM `company_brand` INNER JOIN `company` INNER JOIN `address` INNER JOIN `location` INNER JOIN `city` ON `company_brand`.`company_id` = `company`.`id` AND `company_brand`.`address_id`=`address`.`id` AND `address`.`location_id`=`location`.`id` AND `location`.`city_id`=`city`.`id`;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (data.next()) {
                Vector v = new Vector();

                v.add(data.getString("company_brand.id"));
                v.add(data.getString("company_brand.name"));
                v.add(data.getString("company_brand.mob"));

                v.add(data.getString("company.id"));
                v.add(data.getString("company.name"));
                v.add(data.getString("address.id"));

                v.add(data.getString("address.line_1") + ", " + data.getString("address.line_2") + ", " + data.getString("city.name"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

