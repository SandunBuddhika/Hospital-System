    private void loadCompany() {
        try {

            ResultSet data = MySQL.search("SELECT * FROM `company` INNER JOIN `status` ON `company`.`status_id`=`status`.`id`;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (data.next()) {
                Vector v = new Vector();

                v.add(data.getString("company.id"));
                v.add(data.getString("company.name"));
                v.add(data.getString("company.mob"));
                v.add(data.getString("status.name"));
                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

