    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        
        String process_id = jLabel1.getText();
        String profit = jLabel38.getText();
        String balance = jLabel35.getText();
        String total = jLabel44.getText();
        String payment = jTextField8.getText();
        String selectedItem = jComboBox1.getSelectedItem().toString();
        
        if (Sign_In.USER_TYPE == 0) {
            JOptionPane.showMessageDialog(this, "You don't have a permission to add a user", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (jTable1.getRowCount() == 0) {
            JOptionPane.showMessageDialog(this, "No product are not added to the table", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (selectedItem.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select the payment method", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("([0])|([1-9][0-9]*)|([0][.]([0]*[1-9][0-9]*))|(([1-9][0-9]*)[.]([0]*[1-9][0-9]*))").matcher(payment).matches()) {
            JOptionPane.showMessageDialog(this, "Payment is invalid ", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (jTextField4.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the fee", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (Double.parseDouble(total) > Double.parseDouble(payment)) {
            JOptionPane.showMessageDialog(this, "Payment is low (Rs." + String.valueOf(Double.parseDouble(total) - Double.parseDouble(payment)) + ")", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            long invoice_id = System.currentTimeMillis() + Sign_In.USER_ID;
            long bill_id = System.currentTimeMillis() + Sign_In.USER_ID;
            long profit_id = Sign_In.USER_ID + System.currentTimeMillis();
            SimpleDateFormat sdf2 = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
            
            try {
                ResultSet r1 = MySQL.search("SELECT * FROM `invoice` INNER JOIN `bill` INNER JOIN `appointment_bill` INNER JOIN `process` ON `invoice`.`bill_id`=`bill`.`id` AND `appointment_bill`.`bill_id`=`bill`.`id` AND `appointment_bill`.`process_id`=`process`.`id` WHERE `process`.`id`='" + process_id + "' AND `bill`.`bill_status_id`=(SELECT `id` FROM `bill_status` WHERE `name`='Paid');");
                if (!r1.next()) {
                    
                    MySQL.iud("INSERT INTO `bill` (`id`,`bill_status_id`) VALUES('" + bill_id + "',(SELECT `id` FROM `bill_status` WHERE `name`='Not Paid'))  ;");
                    MySQL.iud("INSERT INTO `appointment_bill` (`bill_id`,`process_id`) VALUES('" + bill_id + "','" + process_id + "');");
                    
                    MySQL.iud("INSERT INTO `invoice` (`id`,`date`,`bill_id`,`user_id`) VALUES('" + invoice_id + "','" + sdf2.format(new Date()) + "','" + bill_id + "','" + Sign_In.USER_ID + "');");
                    MySQL.iud("INSERT INTO `company_profit` (`id`,`profit`,`profit_type_id`) VALUES('" + profit_id + "','" + profit + "',(SELECT `id` FROM `profit_type` WHERE `name`='Invoice'));");
                    MySQL.iud("INSERT INTO `invoice_payment` (`payment`,`balance`,`fee`,`payment_type_id`,`invoice_id`,`company_profit_id`) VALUES ('" + payment + "','" + balance + "','" + profit + "',(SELECT `id` FROM `payment_type` WHERE `name`='" + selectedItem + "'),'" + invoice_id + "','" + profit_id + "') ");
                    
                    Vector<Item_Invoice_Report> v = new Vector();
                    Item_Invoice_Report ivr = null;
                    for (int i = 0; i < jTable1.getRowCount(); i++) {
                        String p_name = jTable1.getValueAt(i, 1).toString();
                        String stock_id = jTable1.getValueAt(i, 4).toString();
                        String ip = jTable1.getValueAt(i, 5).toString();
                        String qty = jTable1.getValueAt(i, 6).toString();
                        String price = jTable1.getValueAt(i, 7).toString();
                        String itemtotal = jTable1.getValueAt(i, 9).toString();
                        
                        ResultSet r = MySQL.search("SELECT * FROM `stock` WHERE `id`='" + stock_id + "';");
                        
                        MySQL.iud("INSERT INTO `invoice_item`(`individual_pieces`,`qty`,`stock_id`,`invoice_id`) VALUES('" + ip + "','" + qty + "','" + stock_id + "','" + invoice_id + "');");
                        if (r.next()) {
                            int newqty = Integer.parseInt(r.getString("qty")) - Integer.parseInt(qty);
                            int newip = Integer.parseInt(r.getString("a_individual_pieces")) - Integer.parseInt(ip);
                            int newIp = Integer.parseInt(qty) * Integer.parseInt(r.getString("individual_pieces")) + Integer.parseInt(ip);
                            MySQL.iud("UPDATE `stock` SET `a_individual_pieces`='" + newip + "',`qty`='" + newqty + "' WHERE `id`='" + stock_id + "';");
                            ivr = new Item_Invoice_Report(p_name, String.valueOf(newIp), price, itemtotal);
                            v.add(ivr);
                        }
                    }
                    MySQL.iud("UPDATE `bill` SET `bill_status_id`=(SELECT `id` FROM `bill_status` WHERE `name`='Paid') WHERE `id`=(SELECT `bill_id` FROM `appointment_bill` WHERE `process_id`='" + process_id + "');");
                    
                    JOptionPane.showMessageDialog(this, "Invoice Successfully added to the system", "Success", JOptionPane.INFORMATION_MESSAGE);
                    
                    String filepath = "src//report//Inovice_R.jrxml";
                    
                    JasperReport jr = JasperCompileManager.compileReport(filepath);
                    
                    HashMap parameters = new HashMap();
                    
                    parameters.put("Parameter1", Double.parseDouble(total));
                    parameters.put("Parameter2", Double.parseDouble(payment));
                    parameters.put("Parameter3", Double.parseDouble(balance));
                    
                    JRBeanCollectionDataSource dataSource = new JRBeanCollectionDataSource(v);
                    
                    JasperPrint jp = JasperFillManager.fillReport(jr, parameters, dataSource);
                    JasperViewer.viewReport(jp, false);
                    
                    reset();
                    
                    jLabel35.setText("0.00");
                    jLabel44.setText("0.00");
                    jLabel38.setText("0.00");
                    jTextField8.setText("");
                    jComboBox1.setSelectedIndex(0);
                    
                    ((DefaultTableModel) jTable1.getModel()).setRowCount(0);
                } else {
                    JOptionPane.showMessageDialog(this, "Already Paid", "WRNING", JOptionPane.WARNING_MESSAGE);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
            
        }
    }//GEN-LAST:event_jButton5ActionPerformed

