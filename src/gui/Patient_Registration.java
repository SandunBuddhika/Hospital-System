    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        String id = jTextField4.getText();
        String f_name = jTextField8.getText();
        String m_name = jTextField9.getText();
        String l_name = jTextField10.getText();
        String mob = jTextField2.getText();
        String gender = jComboBox3.getSelectedItem().toString();
        String status = jComboBox4.getSelectedItem().toString();
        String b_type = jComboBox5.getSelectedItem().toString();
        String dob = "";
        if (jDateChooser2.getDate() != null) {
            dob = sdf.format(jDateChooser2.getDate());
        } else {
            dob = "0000-00-00";
        }

        if (m_name.isEmpty()) {
            m_name = ("-");
        }
        if (l_name.isEmpty()) {
            l_name = ("-");
        }
        if (b_type.equals("Select")) {
            b_type = ("9");
        }

        if (Sign_In.USER_TYPE == 0) {
            JOptionPane.showMessageDialog(this, "You don't have a permission to add a user", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (f_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter patient first name", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (m_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter patient middle name", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (l_name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter patient last name", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (mob.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter patient mobile numder", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (mob.length() != 10) {
            JOptionPane.showMessageDialog(this, "Invalid mobile number1", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (!Pattern.compile("07[1245678][0-9]{7}+").matcher(mob).matches()) {
            JOptionPane.showMessageDialog(this, "Invalid mobile number2", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (gender.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a gender", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (b_type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a blood type", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a status", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else {

            MySQL.iud("UPDATE `patient` SET `f_name`='" + f_name + "' , `m_name`='" + m_name + "' , `l_name`='" + l_name + "' , `dob`='" + dob + "' , `gender_id`=(SELECT `id` FROM `gender` WHERE `name`='" + gender + "') , `status_id`=(SELECT `id` FROM `status` WHERE `name`='" + status + "') , `blood_type_id`=(SELECT `id` FROM `blood_type` WHERE `name`='" + b_type + "') WHERE `id`='" + id + "';");
            toSearch();
            reset();
            jTextField4.setText(id);
            search();
            toUpdate();
            JOptionPane.showMessageDialog(this, "The patient successfully updated to the system", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);

        }
    }//GEN-LAST:event_jButton2ActionPerformed

