    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String name = jTextField1.getText();
        String type = jComboBox1.getSelectedItem().toString();
        String brand = jComboBox2.getSelectedItem().toString();
        String category = jComboBox8.getSelectedItem().toString();
        String company = jComboBox4.getSelectedItem().toString();

        if (Sign_In.USER_TYPE == 0) {
            JOptionPane.showMessageDialog(this, "You don't have a permission to add a user", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (name.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Please enter the item name", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (type.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a type", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (brand.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a brand", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (category.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a category", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (company.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select a company", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else {
            try {

                MySQL.iud("INSERT INTO `item` (`name`,`item_category_type_id`,`item_category_id`,`brand_id`,`item_company_id`) VALUES ('" + name + "',(SELECT `id` FROM `item_category_type` WHERE `name`='" + type + "'),(SELECT `id` FROM `item_category` WHERE `name`='" + category + "'),(SELECT `id` FROM `brand` WHERE `name`='" + brand + "'),(SELECT `id` FROM `item_company` WHERE `name`='" + company + "'));");

                jTextField1.setText("");
                jComboBox1.setSelectedIndex(0);
                jComboBox2.setSelectedIndex(0);
                jComboBox8.setSelectedIndex(0);
                jComboBox4.setSelectedIndex(0);
                jTextField1.grabFocus();
                loadItems();
                JOptionPane.showMessageDialog(this, "New item successfully added to the system", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }//GEN-LAST:event_jButton2ActionPerformed
