    public void search() {
        if (isSearch == 1) {

            Vector querry = new Vector();

            String uid = jTextField1.getText();
            String id = jTextField3.getText();
            String basic = jComboBox2.getSelectedItem().toString();
            String ot = jComboBox3.getSelectedItem().toString();
            String month = jComboBox4.getSelectedItem().toString();
            String total_fr = jTextField6.getText();
            String total_to = jTextField7.getText();

            if (!uid.isEmpty()) {
                querry.add("`user`.`id`LIKE'%" + uid + "%'");
            }
            if (!id.isEmpty()) {
                querry.add("`pay_sheet`.`id`LIKE'%" + id + "%'");
            }
            if (!basic.equals("Select")) {
                querry.add("`salary_basic_type`.`price`='" + basic + "'");
            }
            if (!ot.equals("Select")) {
                querry.add("`OT_type`.`price`='" + ot + "'");
            }
            if (!month.equals("Select")) {
                querry.add("`month`.`name`='" + month + "'");
            }
            if (!total_fr.isEmpty()) {
                querry.add("`pay_sheet`.`total`>='" + total_fr + "'");
            }
            if (!total_to.isEmpty()) {
                querry.add("`pay_sheet`.`total`<='" + total_to + "'");
            }
            int sortBy = jComboBox1.getSelectedIndex();
            String sortquerry = "";
            switch (sortBy) {
                case 0:
                    sortquerry = "`pay_sheet`.`id` ASC";
                    break;
                case 1:
                    sortquerry = "`pay_sheet`.`id` DESC";
                    break;
                case 2:
                    sortquerry = "`month`.`id` ASC";
                    break;
                case 3:
                    sortquerry = "`month`.`id` DESC";
                    break;
                case 4:
                    sortquerry = "`salary_basic_type`.`price` ASC";
                    break;
                case 5:
                    sortquerry = "`salary_basic_type`.`price` DESC";
                    break;
                case 6:
                    sortquerry = "`shift_type`.`price` ASC";
                    break;
                case 7:
                    sortquerry = "`shift_type`.`price` DESC";
                    break;
                case 8:
                    sortquerry = "`pay_sheet`.`total` ASC";
                    break;
                case 9:
                    sortquerry = "`pay_sheet`.`total` DESC";
                    break;
                default:
                    sortquerry = "`pay_sheet`.`id` ASC";
                    break;
            }
            String fixquerry = "";

            for (int j = 0; j < querry.size(); j++) {
                if (j == 0) {
                    fixquerry += "WHERE ";
                }
                fixquerry += querry.get(j) + " ";
                if (j < querry.size() - 1) {
                    fixquerry += "AND ";
                }
            }
            try {
                ResultSet data = MySQL.search("SELECT * FROM `pay_sheet` INNER JOIN `month` INNER JOIN `salary_basic_type` INNER JOIN `OT_type` INNER JOIN `user`  INNER JOIN `company_profit` ON `user`.`id`=`pay_sheet`.`user_id` AND `month`.`id`=`pay_sheet`.`month_id` AND`pay_sheet`.`salary_basic_type_id`=`salary_basic_type`.`id` AND `pay_sheet`.`OT_type_id`=`ot_type`.`id` AND `company_profit`.`id` = `pay_sheet`.`company_profit_id` " + fixquerry + " ORDER BY " + sortquerry + ";");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (data.next()) {
                    Vector v = new Vector();

                    v.add(data.getString("pay_sheet.id"));
                    v.add(data.getString("user.id"));
                    if (data.getString("user.m_name").equals("-")) {
                        v.add(data.getString("user.f_name") + " " + data.getString("user.l_name"));
                    } else {
                        v.add(data.getString("user.f_name") + " " + data.getString("user.m_name") + " " + data.getString("user.l_name"));
                    }
                    v.add(data.getString("salary_basic_type.price"));
                    v.add(data.getString("ot_type.price"));
                    v.add(data.getString("pay_sheet.OT-hours"));
                    v.add(data.getString("company_profit.profit"));
                    v.add(data.getString("pay_sheet.total"));
                    v.add(data.getString("month.name"));
                    v.add(data.getString("pay_sheet.date"));

                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }
