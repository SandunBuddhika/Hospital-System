    private void search() {
        if (isSearch == 1) {
            Vector querry = new Vector();

            String id = jTextField3.getText();
            String name = jTextField1.getText();
            String type = jComboBox1.getSelectedItem().toString();
            String brand = jComboBox2.getSelectedItem().toString();
            String category = jComboBox8.getSelectedItem().toString();
            String company = jComboBox4.getSelectedItem().toString();

            if (!id.isEmpty()) {
                querry.add("`item`.`id`LIKE'%" + id + "%'");
            }
            if (!name.isEmpty()) {
                querry.add("`item`.`name`LIKE'%" + name + "%'");
            }
            if (!type.equals("Select")) {
                querry.add("`item_category_type`.`name`='" + type + "'");
            }
            if (!brand.equals("Select")) {
                querry.add("`brand`.`name`='" + brand + "'");
            }
            if (!category.equals("Select")) {
                querry.add("`item_category`.`name`='" + category + "'");
            }
            if (!company.equals("Select")) {
                querry.add("`item_company`.`name`='" + company + "'");
            }

            int sortBy = jComboBox5.getSelectedIndex();
            String sortquerry = "";
            switch (sortBy) {
                case 0:
                    sortquerry = "`item`.`id` ASC";
                    break;
                case 1:
                    sortquerry = "`item`.`id` DESC";
                    break;
                case 2:
                    sortquerry = "`item`.`name` ASC";
                    break;
                case 3:
                    sortquerry = "`item`.`name` DESC";
                    break;
                case 4:
                    sortquerry = "`brand`.`name` ASC";
                    break;
                case 5:
                    sortquerry = "`brand`.`name` DESC";
                    break;
                case 6:
                    sortquerry = "`item_category`.`name` ASC";
                    break;
                case 7:
                    sortquerry = "`item_category`.`name` DESC";
                    break;
                case 8:
                    sortquerry = "`item_company`.`name` ASC";
                    break;
                case 9:
                    sortquerry = "`item_company`.`name` DESC";
                    break;
                default:
                    sortquerry = "`item`.`id` ASC";
                    break;
            }
            String fixquerry = "";

            for (int j = 0; j < querry.size(); j++) {
                if (j == 0) {
                    fixquerry += "WHERE ";
                }
                fixquerry += querry.get(j) + " ";
                if (j < querry.size() - 1) {
                    fixquerry += "AND ";
                }
            }

            try {
                ResultSet data = MySQL.search("SELECT * FROM `item` INNER JOIN `item_category_type` INNER JOIN `item_category` INNER JOIN `brand` INNER JOIN `item_company` ON `item`.`item_category_type_id`=`item_category_type`.`id` AND `item`.`item_category_id`=`item_category`.`id` AND `item`.`brand_id`=`brand`.`id`AND `item`.`item_company_id`=`item_company`.`id` " + fixquerry + " ORDER BY " + sortquerry + ";");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (data.next()) {
                    Vector v = new Vector();

                    v.add(data.getString("item.id"));
                    v.add(data.getString("item.name"));
                    v.add(data.getString("item_category_type.name"));
                    v.add(data.getString("item_category.name"));
                    v.add(data.getString("brand.name"));
                    v.add(data.getString("item_company.name"));
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

