                                double o1 = Double.parseDouble(a.getString("attendance.e_time").substring(0, 5).replace(":", ".")) - Double.parseDouble(a.getString("attendance.s_time").substring(0, 5).replace(":", "."));
                                basic_time += b1;
                                work_time += o1;
