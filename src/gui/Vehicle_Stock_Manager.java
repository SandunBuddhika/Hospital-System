    private void jButton8ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton8ActionPerformed
        String s_id = jLabel2.getText();
        String number = jTextField2.getText();
        String v_id = jTextField4.getText();
        String v_name = jTextField8.getText();
        String company = jComboBox3.getSelectedItem().toString();
        String brand = jComboBox4.getSelectedItem().toString();
        String price = jTextField1.getText();
        String status = jComboBox1.getSelectedItem().toString();

        Date mfd = null;
        if (jDateChooser2.getDate() != null) {
            mfd = jDateChooser2.getDate();
        }
        
        if (Sign_In.USER_TYPE == 0) {
            JOptionPane.showMessageDialog(this, "You don't have a permission to add a user", "WARNING", JOptionPane.WARNING_MESSAGE);
        } else if (v_id.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select the vehicle", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (v_name.equals("None")) {
            JOptionPane.showMessageDialog(this, "Please select the vehicle", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (price.isBlank()) {
            JOptionPane.showMessageDialog(this, "Please enter the price", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mfd == null) {
            JOptionPane.showMessageDialog(this, "Please enter the mfd", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (mfd.after(new Date())) {
            JOptionPane.showMessageDialog(this, "Invalid mfd", "Warning", JOptionPane.WARNING_MESSAGE);
        } else if (status.equals("Select")) {
            JOptionPane.showMessageDialog(this, "Please select the vehicle status", "Warning", JOptionPane.WARNING_MESSAGE);
        } else {
            String supplier_q = "";
            String item_q = "";

            int sq = JOptionPane.showConfirmDialog(this, "Do you want to change the supplier?", "QUESTION", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (sq == JOptionPane.YES_OPTION) {
                if (s_id.equals("None")) {
                    JOptionPane.showMessageDialog(this, "Please select the suppiler", "Warning", JOptionPane.WARNING_MESSAGE);
                } else {
                    try {
                        ResultSet data = MySQL.search("UPDATE `vehicle_grn` SET `vehicle_grn`.`suppiler_id`='" + s_id + "' WHERE `vehicle_grn`.`id`=(SELECT `vehicle_grn`.`id` FROM `vehicle_grn` INNER JOIN `vehicle_grn_item` ON `vehicle_grn`.`id`=`vehicle_grn_item`.`vehicle_grn_id` WHERE `vehicle_grn`.`stock_id`=(SELECT `stock_id` FROM `stock_has_vehicle` WHERE `stock_has_vehicle`.`numbe`='" + number + "')) ;");
                    } catch (Exception e) {
                        e.printStackTrace();
                    }
                }
            }
            int iq = JOptionPane.showConfirmDialog(this, "Do you want to change the vehicle also?", "QUESTION", JOptionPane.YES_NO_OPTION, JOptionPane.INFORMATION_MESSAGE);
            if (iq == JOptionPane.YES_OPTION) {
                item_q = "`vehicle_id`='" + v_id + "',";
            }
            MySQL.iud("UPDATE `vehicle_stock` SET " + item_q + " `price`='" + price + "',`MFD`='" + sdf.format(mfd) + "' WHERE `id`=(SELECT `stock_id` FROM `stock_has_vehicle` WHERE `stock_has_vehicle`.`number`='" + number + "');");
            MySQL.iud("UPDATE `stock_has_vehicle` SET `vehicle_status_id`=(SELECT `id` FROM `vehicle_status` WHERE `name`='" + status + "') WHERE `number`='" + number + "';");
            JOptionPane.showMessageDialog(this, "The vehicle Updated", "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
            toSearch();
            reset();
            jTextField2.setText(number);
            search();
        }
    }//GEN-LAST:event_jButton8ActionPerformed

