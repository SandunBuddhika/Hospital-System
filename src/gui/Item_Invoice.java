    private void updateTotal() {
        double total = 0.00;
        
        for (int i = 0; i < jTable1.getRowCount(); i++) {
            String t = jTable1.getValueAt(i, 9).toString();
            total = total + Double.parseDouble(t) + Double.parseDouble(jLabel38.getText());
        }
        jLabel44.setText(this.df.format(total));
    }
    
    private void balance() {
        if (jTextField8.getText().isEmpty()) {
            jLabel35.setText("0.00");
            jLabel35.setForeground(Color.GREEN);
        } else {
            String total = jLabel44.getText();
            String payment = jTextField8.getText();
            double balance = Double.parseDouble(payment) - Double.parseDouble(total);
            if (balance < 0) {
                jLabel35.setForeground(Color.RED);
            } else {
                jLabel35.setForeground(Color.GREEN);
            }
            jLabel35.setText(df.format(balance));
        }
    }
    
    private void profit() {
        if (!jTextField4.getText().isEmpty()) {
            jLabel38.setText(this.df.format(Double.parseDouble(jTextField4.getText())));
        }
    }
    
