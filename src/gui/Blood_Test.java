    public void search() {
        if (isSearch == 1) {
            Vector querry = new Vector();
            String id = jTextField5.getText();
            String a_id = jLabel2.getText();
            String text = jTextField8.getText();
            String room = jLabel14.getText();

            if (!id.isEmpty()) {
                querry.add("`process`.`id`LIKE'%" + id + "%'");
            }
            if (!text.isEmpty()) {
                querry.add("`process`.`text`LIKE'%" + text + "%'");
            }
            if (!a_id.equals("None")) {
                querry.add("`appointment`.`id`='" + a_id + "'");
            }
            if (!room.equals("None")) {
                querry.add("`room`.`id`='" + room + "'");
            }

            int sortBy = jComboBox2.getSelectedIndex();
            String sortquerry = "";
            switch (sortBy) {
                case 0:
                    sortquerry = "`process`.`id` ASC";
                    break;
                case 1:
                    sortquerry = "`process`.`id` DESC";
                    break;
                case 2:
                    sortquerry = "`process`.`text` ASC";
                    break;
                case 3:
                    sortquerry = "`process`.`text` DESC";
                    break;
                default:
                    sortquerry = "`process`.`id` ASC";
                    break;
            }
            String fixquerry = "";

            for (int j = 0; j < querry.size(); j++) {
                if (j == 0) {
                    fixquerry += "WHERE ";
                }
                fixquerry += querry.get(j) + " ";
                if (j < querry.size() - 1) {
                    fixquerry += "AND ";
                }
            }
            try {
                ResultSet data = MySQL.search("SELECT * FROM `process` INNER JOIN `appointment` INNER JOIN `room` INNER JOIN `patient` ON `process`.`appointment_id`=`appointment`.`id` AND `room`.`id`=`process`.`room_id` AND `patient`.`id` = `appointment`.`patient_id` " + fixquerry + " ORDER BY " + sortquerry + " ;");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (data.next()) {
                    Vector v = new Vector();

                    v.add(data.getString("process.id"));
                    v.add(data.getString("process.text"));
                    v.add(data.getString("appointment.id"));
                    v.add(data.getString("appointment.case"));
                    v.add(data.getString("patient.id"));
                    if (data.getString("patient.m_name").equals("-")) {
                        if (!data.getString("patient.l_name").equals("-")) {
                            v.add(data.getString("patient.f_name") + " " + data.getString("patient.l_name"));
                        } else {
                            v.add(data.getString("patient.f_name"));
                        }
                    } else {
                        if (!data.getString("patient.l_name").equals("-")) {
                            v.add(data.getString("patient.f_name") + " " + data.getString("patient.m_name") + " " + data.getString("patient.l_name"));
                        } else {
                            v.add(data.getString("patient.f_name") + " " + data.getString("patient.m_name"));
                        }
                    }
                    v.add(data.getString("room.id"));
                    v.add(data.getString("room.name"));
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }

        }
    }

