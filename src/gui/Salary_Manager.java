    private void loadPaySlip() {
        try {

            ResultSet data = MySQL.search("SELECT * FROM `pay_sheet` INNER JOIN `month` INNER JOIN `salary_basic_type` INNER JOIN `OT_type` INNER JOIN `user`  INNER JOIN `company_profit` ON `user`.`id`=`pay_sheet`.`user_id` AND `month`.`id`=`pay_sheet`.`month_id` AND`pay_sheet`.`salary_basic_type_id`=`salary_basic_type`.`id` AND `pay_sheet`.`OT_type_id`=`ot_type`.`id` AND `company_profit`.`id` = `pay_sheet`.`company_profit_id` ORDER BY `pay_sheet`.`date` ASC;");
            DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
            dtm.setRowCount(0);
            while (data.next()) {
                Vector v = new Vector();

                v.add(data.getString("pay_sheet.id"));
                v.add(data.getString("user.id"));
                if (data.getString("user.m_name").equals("-")) {
                    v.add(data.getString("user.f_name") + " " + data.getString("user.l_name"));
                } else {
                    v.add(data.getString("user.f_name") + " " + data.getString("user.m_name") + " " + data.getString("user.l_name"));
                }
                v.add(data.getString("salary_basic_type.price"));
                v.add(data.getString("ot_type.price"));
                v.add(data.getString("pay_sheet.OT-hours"));
                v.add(data.getString("company_profit.profit"));
                v.add(data.getString("pay_sheet.total"));
                v.add(data.getString("month.name"));
                v.add(data.getString("pay_sheet.date"));

                dtm.addRow(v);
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }

