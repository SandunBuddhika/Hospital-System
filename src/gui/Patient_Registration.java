    private void search() {
        if (isSearch == 1) {

            Vector querry = new Vector();

            String id = jTextField4.getText();
            String fname = jTextField8.getText();
            String mname = jTextField9.getText();
            String lname = jTextField10.getText();
            String mob = jTextField2.getText();
            String gender = jComboBox3.getSelectedItem().toString();
            String status = jComboBox4.getSelectedItem().toString();
            String b_type = jComboBox5.getSelectedItem().toString();
            Date dobfr = null;
            Date dobto = null;
            if (jDateChooser2.getDate() != null) {
                dobfr = jDateChooser2.getDate();
            }
            if (jDateChooser1.getDate() != null) {
                dobto = jDateChooser1.getDate();
            }

            if (!id.isEmpty()) {
                querry.add("`patient`.`id`LIKE'%" + id + "%'");
            }
            if (!fname.isEmpty()) {
                querry.add("`patient`.`f_name`LIKE'%" + fname + "%'");
            }
            if (!mname.isEmpty()) {
                querry.add("`patient`.`m_name`LIKE'%" + mname + "%'");
            }
            if (!lname.isEmpty()) {
                querry.add("`patient`.`l_name`LIKE'%" + lname + "%'");
            }
            if (dobfr != null) {
                querry.add("`patient`.`dob`>='" + sdf.format(dobfr) + "'");
            }
            if (dobto != null) {
                querry.add("`patient`.`dob`<='" + sdf.format(dobto) + "'");
            }
            if (!mob.isEmpty()) {
                querry.add("`patient`.`mob`LIKE'%" + mob + "%'");
            }
            if (!gender.equals("Select")) {
                querry.add("`gender`.`name`='" + gender + "'");
            }
            if (!status.equals("Select")) {
                querry.add("`status`.`name`='" + status + "'");
            }
            if (!b_type.equals("Select")) {
                querry.add("`blood_type`.`name`='" + b_type + "'");
            }

            int sortBy = jComboBox2.getSelectedIndex();
            String sortquerry = "";
            switch (sortBy) {
                case 0:
                    sortquerry = "`patient`.`id` ASC";
                    break;
                case 1:
                    sortquerry = "`patient`.`id` DESC";
                    break;
                case 2:
                    sortquerry = "`patient`.`f_name` ASC";
                    break;
                case 3:
                    sortquerry = "`patient`.`f_name` DESC";
                    break;
                case 4:
                    sortquerry = "`patient`.`m_name` ASC";
                    break;
                case 5:
                    sortquerry = "`patient`.`m_name` DESC";
                    break;
                case 6:
                    sortquerry = "`patient`.`l_name` ASC";
                    break;
                case 7:
                    sortquerry = "`patient`.`l_name` DESC";
                    break;
                case 8:
                    sortquerry = "`patient`.`mob` ASC";
                    break;
                case 9:
                    sortquerry = "`patient`.`mob` DESC";
                    break;
                case 10:
                    sortquerry = "`patient`.`dob` ASC";
                    break;
                case 11:
                    sortquerry = "`patient`.`dob` DESC";
                    break;
                default:
                    sortquerry = "`patient`.`id` ASC";
                    break;
            }
            String fixquerry = "";

            for (int j = 0; j < querry.size(); j++) {
                if (j == 0) {
                    fixquerry += "WHERE ";
                }
                fixquerry += querry.get(j) + " ";
                if (j < querry.size() - 1) {
                    fixquerry += "AND ";
                }
            }

            try {
                ResultSet data = MySQL.search("SELECT * FROM `patient` INNER JOIN `gender` INNER JOIN `status` INNER JOIN `blood_type` ON `patient`.`gender_id`=`gender`.`id` AND `patient`.`status_id` = `status`.`id` AND `patient`.`blood_type_id` =`blood_type`.`id` " + fixquerry + " ORDER BY " + sortquerry + " ;");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (data.next()) {
                    Vector v = new Vector();

                    v.add(data.getString("patient.id"));

                    if (data.getString("patient.m_name").equals("-")) {
                        if (!data.getString("patient.l_name").equals("-")) {
                            v.add(data.getString("patient.f_name") + " " + data.getString("patient.l_name"));
                        } else {
                            v.add(data.getString("patient.f_name"));
                        }
                    } else {
                        if (!data.getString("patient.l_name").equals("-")) {
                            v.add(data.getString("patient.f_name") + " " + data.getString("patient.m_name") + " " + data.getString("patient.l_name"));
                        } else {
                            v.add(data.getString("patient.f_name") + " " + data.getString("patient.m_name"));
                        }
                    }
                    v.add(data.getString("patient.dob"));
                    v.add(data.getString("patient.mob"));
                    v.add(data.getString("gender.name"));
                    v.add(data.getString("status.name"));
                    v.add(data.getString("blood_type.name"));
                    v.add(data.getString("patient.date"));
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }


