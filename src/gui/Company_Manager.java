    private void loadStatus() {

        try {
            ResultSet data = MySQL.search("SELECT * FROM `status`");
            Vector v = new Vector();
            v.add("Select");
            while (data.next()) {
                v.add(data.getString("name"));
            }
            DefaultComboBoxModel dcm = new DefaultComboBoxModel(v);
            jComboBox2.setModel(dcm);
        } catch (Exception e) {
            e.printStackTrace();
        }

    }

    private void search() {
        if (isSearch == 1) {
            Vector querry = new Vector();

            String id = jTextField1.getText();
            String name = jTextField6.getText();
            String mob = jTextField2.getText();
            String status = jComboBox2.getSelectedItem().toString();

            if (!id.isEmpty()) {
                querry.add("`company`.`id`LIKE'%" + id + "%'");
            }
            if (!name.isEmpty()) {
                querry.add("`company`.`name`LIKE'%" + name + "%'");
            }
            if (!mob.isEmpty()) {
                querry.add("`company`.`mob`LIKE'%" + mob + "%'");
            }
            if (!status.isEmpty()) {
                querry.add("`status`.`name`='" + status + "'");
            }

            int sortBy = jComboBox1.getSelectedIndex();
            String sortquerry = "";
            switch (sortBy) {
                case 0:
                    sortquerry = "`company`.`id` ASC";
                    break;
                case 1:
                    sortquerry = "`company`.`id` DESC";
                    break;
                case 2:
                    sortquerry = "`company`.`name` ASC";
                    break;
                case 3:
                    sortquerry = "`company`.`name` DESC";
                    break;
                case 4:
                    sortquerry = "`company`.`mob` ASC";
                    break;
                case 5:
                    sortquerry = "`company`.`mob` DESC";
                    break;
                default:
                    sortquerry = "`company`.`id` ASC";
                    break;
            }
            String fixquerry = "";

            for (int j = 0; j < querry.size(); j++) {
                if (j == 0) {
                    fixquerry += "WHERE ";
                }
                fixquerry += querry.get(j) + " ";
                if (j < querry.size() - 1) {
                    fixquerry += "AND ";
                }
            }
            try {
                ResultSet data = MySQL.search("SELECT * FROM `company` INNER JOIN `status` ON `company`.`status_id`=`status`.`id` " + fixquerry + " ORDER BY " + sortquerry + " ;");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (data.next()) {
                    Vector v = new Vector();

                    v.add(data.getString("company.id"));
                    v.add(data.getString("company.name"));
                    v.add(data.getString("company.mob"));
                    v.add(data.getString("status.name"));

                    dtm.addRow(v);
                }

            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }


