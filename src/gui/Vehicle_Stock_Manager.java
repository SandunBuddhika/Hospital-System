    public void search() {
        if (isSearch == 1) {
            try {
                SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");

                String s_id = jLabel2.getText();
                String price_fr = jTextField1.getText();
                String number = jTextField2.getText();
                String v_id = jTextField4.getText();
                String price_to = jTextField6.getText();
                String v_name = jTextField8.getText();
                String status = jComboBox1.getSelectedItem().toString();
                String company = jComboBox3.getSelectedItem().toString();
                String brand = jComboBox4.getSelectedItem().toString();

                String mfd_fr = null;
                String mfd_to = null;
                if (jDateChooser3.getDate() != null) {
                    mfd_to = sdf.format(jDateChooser3.getDate());
                }
                if (jDateChooser2.getDate() != null) {
                    mfd_fr = sdf.format(jDateChooser2.getDate());
                }
                int sortBy = jComboBox2.getSelectedIndex();

                Vector querryVector = new Vector();

                if (!s_id.endsWith("None")) {
                    querryVector.add("`supplier`.`id`LIKE'%" + s_id + "%'");
                }
                if (!number.isEmpty()) {
                    querryVector.add("`stock_has_vehicle`.`number`LIKE'%" + number + "%'");
                }
                if (!v_id.isEmpty()) {
                    querryVector.add("`vehicle`.`id`LIKE'%" + v_id + "%'");
                }
                if (!v_name.isEmpty()) {
                    querryVector.add("`vehicle`.`name`LIKE'%" + v_name + "%'");
                }
                if (!company.equals("Select")) {
                    querryVector.add("`vehicle_compny`.`name`='" + company + "'");
                }
                if (!brand.equals("Select")) {
                    querryVector.add("`vehicle_brand`.`name`='" + brand + "'");
                }
                if (!status.equals("Select")) {
                    querryVector.add("`vehicle_status`.`name`='" + status + "'");
                }
                if (!price_fr.isEmpty()) {
                    querryVector.add("`vehicle_stock`.`price`>='" + price_fr + "'");
                }
                if (!price_to.isEmpty()) {
                    querryVector.add("`vehicle_stock`.`price`<='" + price_to + "'");
                }
                if (mfd_fr != null) {
                    querryVector.add("`vehicle_stock`.`mfd`>='" + mfd_fr + "'");
                }
                if (mfd_to != null) {
                    querryVector.add("`vehicle_stock`.`mfd`<='" + mfd_to + "'");
                }

                //Order by querry part
                String sortquerry;

                switch (sortBy) {
                    case 0:
                        sortquerry = "`stock_has_vehicle`.`number` ASC";
                        break;
                    case 1:
                        sortquerry = "`stock_has_vehicle`.`number` DESC";
                        break;
                    case 2:
                        sortquerry = "`vehicle`.`name` ASC";
                        break;
                    case 3:
                        sortquerry = "`vehicle`.`name` DESC";
                        break;
                    case 4:
                        sortquerry = "`vehicle_stock`.`price` ASC";
                        break;
                    case 5:
                        sortquerry = "`vehicle_stock`.`price` DESC";
                        break;
                    case 6:
                        sortquerry = "`vehicle_stock`.`MFD` ASC";
                        break;
                    case 7:
                        sortquerry = "`vehicle_stock`.`MFD` DESC";
                        break;
                    default:
                        sortquerry = "`stock_has_vehicle`.`number` ASC";
                        break;
                }

                String whereQuerry = "";

                for (int i = 0; i < querryVector.size(); i++) {
                    if (i == 0) {
                        whereQuerry += "WHERE";
                    }
                    whereQuerry += " " + querryVector.get(i) + " ";
                    if (i != querryVector.size() - 1) {
                        whereQuerry += "AND";
                    }
                }

                ResultSet data = MySQL.search("SELECT * FROM `vehicle_stock` INNER JOIN `stock_has_vehicle` INNER JOIN `vehicle_grn` INNER JOIN `vehicle_grn_item` INNER JOIN `supplier` INNER JOIN `vehicle` INNER JOIN `vehicle_status` INNER JOIN `vehicle_brand` INNER JOIN `vehicle_compny` ON `vehicle_stock`.`id`=`stock_has_vehicle`.`stock_id` AND `vehicle`.`id`=`vehicle_stock`.`vehicle_id` AND `stock_has_vehicle`.`vehicle_status_id`=`vehicle_status`.`id` AND `vehicle_brand`.`id`=`vehicle`.`vehicle_brand_id` AND `vehicle_compny`.`id`=`vehicle`.`vehicle_compny_id` AND `vehicle_grn_item`.`stock_id`=`vehicle_stock`.`id` AND `vehicle_grn`.`id`=`vehicle_grn_item`.`vehicle_grn_id` AND `supplier`.`id`=`vehicle_grn`.`suppiler_id` " + whereQuerry + "  ORDER BY  " + sortquerry + ";");
                DefaultTableModel dtm = (DefaultTableModel) jTable1.getModel();
                dtm.setRowCount(0);
                while (data.next()) {
                    Vector v = new Vector();
                    v.add(data.getString("vehicle.id"));
                    v.add(data.getString("vehicle.name"));
                    v.add(data.getString("vehicle_compny.name"));
                    v.add(data.getString("vehicle_brand.name"));
                    v.add(data.getString("stock_has_vehicle.number"));
                    v.add(data.getString("vehicle_stock.fuel_ capacity"));
                    v.add(data.getString("vehicle_stock.price"));
                    v.add(data.getString("vehicle_stock.MFD"));
                    v.add(data.getString("vehicle_status.name"));
                    dtm.addRow(v);
                }
            } catch (Exception e) {
                e.printStackTrace();
            }
        }
    }

